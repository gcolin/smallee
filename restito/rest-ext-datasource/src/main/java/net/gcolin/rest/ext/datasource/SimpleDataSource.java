package net.gcolin.rest.ext.datasource;

import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;

import javax.activation.DataSource;

/**
 * A DataSource generated by DataSourceProvider.
 * 
 * @author GaÃ«l COLIN
 * @since 1.0
 */
public class SimpleDataSource implements DataSource {

  private String contentType;
  private String name;
  private InputStream inputStream;

  /**
   * Create a read only data source.
   * 
   * @param contentType the type of data source
   * @param name the name of data source
   * @param inputStream an input stream
   */
  public SimpleDataSource(String contentType, String name, InputStream inputStream) {
    this.contentType = contentType;
    this.name = name;
    this.inputStream = inputStream;
  }

  @Override
  public String getContentType() {
    return contentType;
  }

  @Override
  public InputStream getInputStream() throws IOException {
    return inputStream;
  }

  @Override
  public String getName() {
    return name;
  }

  @Override
  public OutputStream getOutputStream() throws IOException {
    throw new IOException("cannot do this");
  }

}
